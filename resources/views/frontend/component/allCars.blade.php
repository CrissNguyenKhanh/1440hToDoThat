$(document).ready(function() {
      // Hiệu ứng cuộn
      $(window).scroll(function() {
          let scroll = $(this).scrollTop(); // Lấy vị trí cuộn
  
          if (scroll > 50) {
              $('.thanhthuhai').css({
                  'position': 'fixed', // Đặt thanh ở vị trí cố định
                  'top': '0', // Đặt thanh ở đầu trang
                  'width': '100%', // Đảm bảo thanh chiếm toàn bộ chiều rộng
                  'z-index': '1000' // Đặt z-index cao để không bị che khuất
              });
              $('.thanhnavbar').css({
                  'background-color': '#333', // Màu nền tối hơn khi cuộn
                  'color': '#fff' // Đổi màu văn bản cho rõ ràng hơn
              });
          } else {
              $('.thanhthuhai').css({
                  'position': 'relative', // Đặt lại vị trí khi cuộn lên
                  'top': 'auto',
                  'width': 'auto'
                  
  
                  
              });
              $('.thanhnavbar').css({
                  'background-color': '#000', // Trả về màu gốc
                  'color': '#f0f0f0' // Trả về màu văn bản gốc
              });
        
          }
         
  
          // Đảm bảo thanh vẫn hiển thị
          
      });
  
      // Hiệu ứng di chuột cho các thanh
      $('.thanhdautien, .thanhthuhai').hover(
          function() {
              $(this).css('box-shadow', '0 12px 24px rgba(0, 0, 0, 0.3)'); // Đổ bóng mạnh hơn khi hover
          }, function() {
              $(this).css('box-shadow', '0 6px 12px rgba(0, 0, 0, 0.2)'); // Đổ bóng nhẹ lại khi không hover
          }
      );
  
      // Hiệu ứng nhấp nháy cho thanh navbar khi cuộn xuống
      $(window).scroll(function() {
          if ($(this).scrollTop() > 50) {
              $('.thanhnavbar').fadeIn(200); // Hiện thanh navbar
          } else {
              $('.thanhnavbar').fadeOut(200); // Ẩn thanh navbar
          }
      });
  });
